package com.wangfeixixi.base.debug.log;

 class InnerJSONException extends Exception {

  public InnerJSONException(String s) {
    super(s);
  }

  public InnerJSONException(String message, Throwable cause) {
    super(message, cause);
  }

  public InnerJSONException(Throwable cause) {
    super(cause);
  }

}

